public class Model {
	private String interfaceName;
	private String interfaceAddress;
	private String interfaceNewAddress;
	private String interfaceWay;
	private String interfaceDetail;

	public String getInterfaceNewAddress() {
		return interfaceNewAddress;
	}

	public void setInterfaceNewAddress(String interfaceNewAddress) {
		this.interfaceNewAddress = interfaceNewAddress;
	}

	public String getInterfaceName() {
		return interfaceName;
	}

	public void setInterfaceName(String interfaceName) {
		this.interfaceName = interfaceName;
	}

	public String getInterfaceAddress() {
		return interfaceAddress;
	}

	public void setInterfaceAddress(String interfaceAddress) {
		this.interfaceAddress = interfaceAddress;
	}

	public String getInterfaceWay() {
		return interfaceWay;
	}

	public void setInterfaceWay(String interfaceWay) {
		this.interfaceWay = interfaceWay;
	}

	public String getInterfaceDetail() {
		return interfaceDetail;
	}

	public void setInterfaceDetail(String interfaceDetail) {
		this.interfaceDetail = interfaceDetail;
	}

}