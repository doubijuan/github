package pers.edward.androidtool.model;

public class NetworkUrlModel {
	// 接口名称
	private String urlName;
	// 接口地址
	private String urlAddress;
	// 接口描述
	private String urlDescribe;
	// 接口请求方式
	private String urlRequestWay;

	public String getUrlName() {
		return urlName;
	}

	public void setUrlName(String urlName) {
		this.urlName = urlName;
	}

	public String getUrlAddress() {
		return urlAddress;
	}

	public void setUrlAddress(String urlAddress) {
		this.urlAddress = urlAddress;
	}

	public String getUrlDescribe() {
		return urlDescribe;
	}

	public void setUrlDescribe(String urlDescribe) {
		this.urlDescribe = urlDescribe;
	}

	public String getUrlRequestWay() {
		return urlRequestWay;
	}

	public void setUrlRequestWay(String urlRequestWay) {
		this.urlRequestWay = urlRequestWay;
	}

}
